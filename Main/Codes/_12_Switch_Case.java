package Main.Codes;

public class _12_Switch_Case {
    public static void main(String[] args) {
        String str="Anirban";

        switch (str) {
            case "Anirban" -> System.out.println("It's Anirban");
            case "Subham" -> System.out.println("It's Subham");
            case "Rahul" -> System.out.println("It's Rahul");
            default -> System.out.println("Enjoy..!!");
        }
    }
}
