public class Switch_case {
    public static void main(String[] args) {
        String str="Rahul";
        // String str="Anirban";
        switch (str) {
            case "Anirban":
                System.out.println("Anirban");
                break;

            case "Rahul":
                System.out.println("Rahul");
                break;
        
            default:
                System.out.println("Default");
                break;
        }
    }
}

