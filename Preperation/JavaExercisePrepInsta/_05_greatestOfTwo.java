public class _05_greatestOfTwo {
    public static void main(String[] args) {
        int a=20;
        int b=20;
        if(a>20){
            System.out.println("A > B");
        }
        else if(a<b){
            System.out.println("A < B");
        }
        else{
            System.out.println("A = B");
        }
    }
}
